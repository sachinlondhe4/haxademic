package com.haxademic.core.data;

import com.haxademic.core.math.MathUtil;

public class Point3D {
	public float x, y, z;
		   
	public Point3D( float x, float y, float z ) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public void easeToPoint( Point3D targetPoint, float easeFactor ) {
		x = MathUtil.easeTo( x, targetPoint.x, easeFactor );
		y = MathUtil.easeTo( y, targetPoint.y, easeFactor );
		z = MathUtil.easeTo( z, targetPoint.z, easeFactor );
	}
}
